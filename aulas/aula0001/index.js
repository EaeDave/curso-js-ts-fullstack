console.log('David "Rodrigues" da Silva');  /* Imprimindo valores literais com console.log() */
console.log("David 'Rodrigues' da Silva");  /* Strings podem ser informadas tanto com aspas simples ou duplas */
console.log(`'David' "Rodrigues" da Silva`)  /* A crase é utilizada para declarar template strings, também é possível
exibir no contexto aspas simples e duplas */

